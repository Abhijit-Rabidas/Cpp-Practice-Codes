// C++ Program to Add Two Numbers

#include <bits/stdc++.h>
using namespace std;

int main()
{
    int num1, num2, sum;

    cout << "Enter the first number: ";
    cin >> num1;
    cout << "Enter the second number: ";
    cin >> num2;

    sum = num1 + num2;

    cout << "sum of " << num1 << " + " << num2 << " = " << sum;
}

