// C++ Program to Multiply two Numbers

#include <iostream>
using namespace std;

int main()
{
    float num1, num2, product;

    cout << "Enter first number: ";
    cin >> num1;

    cout << "Enter second number: ";
    cin >> num2;

    product = num1 * num2;

    cout << num1 << " * " << num2 << " = " << product ;
    return 0;
}